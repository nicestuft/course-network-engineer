"""
Задание 6.2

Заменить в строке line все гласные в нижнем регистре на гласные в верхнем регистре.
Измененную строку записать в переменную result и вывести на стандартный поток
вывода (stdout) с помощью print.

Пример итогового вывода:
$ python task_6_2.py
GUIdO vAn ROssUm bEgAn wOrkIng On PYthOn In thE lAtE 1980s

Строку line нельзя менять вручную, все изменения надо сделать с помощью Python.
"""

line = "Guido van Rossum began working on Python in the late 1980s"
result = ""

for i in line:
	if i in "aeiouy":
		result += i.upper()
	else:
		result += i
		
print(result)
		
	

